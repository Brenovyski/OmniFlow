const I18nContext = React.createContext({ lang: "en", t: (k) => k });

const I18nProvider = ({ lang, children }) => {
  const t = React.useCallback((key, vars) => {
    const dict = STRINGS[lang] || STRINGS.en;
    let s = dict[key] ?? STRINGS.en[key] ?? key;
    if (vars) {
      Object.keys(vars).forEach(v => { s = s.replace(`{${v}}`, vars[v]); });
    }
    return s;
  }, [lang]);
  return <I18nContext.Provider value={{ lang, t }}>{children}</I18nContext.Provider>;
};

const useT = () => React.useContext(I18nContext);

Object.assign(window, { STRINGS, I18nProvider, I18nContext, useT });
